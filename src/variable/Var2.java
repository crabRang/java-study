package variable;

public class Var2 {
    public static void main(String[] args) {
        int a; //변수 선언
        a = 10; //변수 초기화, 정수(int)을 담을 수 있는 변수 a에 10이라는 데이터를 넣음
        System.out.println(a);
        System.out.println(a);
        System.out.println(a);
        //출력할 값을 변경하고 싶은 경우 a의 값반 변경해주면 된다.
    }
}
