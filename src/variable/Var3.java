package variable;

public class Var3 {
    public static void main(String[] args) {
        int a; //변수 선언
        a = 10; //변수 초기화 : a(10)
        System.out.println(a); // 10출력
        a = 50; //변수 값 변경 : a(10 -> 50)
        System.out.println(a); // 50출력
        //프로그램은 한 줄씩 순서대로 실행
        //변수의 값을 변경하면 변수에 들어있던 기존 값은 삭제된다.
    }
}
