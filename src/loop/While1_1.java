package loop;

public class While1_1 {
    public static void main(String[] args) {
        int count = 0;

        count ++;
        System.out.println("현재 숫자는 "+count);
        count ++;
        System.out.println("현재 숫자는 "+count);
        count ++;
        System.out.println("현재 숫자는 "+count);

        //단순히 count에 값을 1씩 3번 더하는 코드
        //같은 코드가 반복되고 있음 -> 반복문 사용
        //반복문의 종류 : while, do~while, for
    }
}
