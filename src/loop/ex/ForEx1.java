package loop.ex;

public class ForEx1 {
    public static void main(String[] args) {
        /*
         * 문제1. 10개의 자연수를 출력하는 프로그램
         * 변수 : count , for문과 while문 둘다 만들 것
         */
        //for문
        for(int count=1; count<=10; count++){
            System.out.println(count);
        }
    }
}
